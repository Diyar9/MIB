    private void loggainbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loggainbtnActionPerformed
        Connection connection;
        PreparedStatement ps;

        try {
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/mibdb","mibdba","mibkey");
            ps = connection.prepareStatement("Select * from agent where Agent_ID=? and Losenord=? and Administrator='J'");
            ps.setString(1, anv_namn.getText());
            ps.setString(2, los_ord.getText());
            ResultSet result = ps.executeQuery();
            if(result.next()){
                JOptionPane.showMessageDialog(null, "Login Success.");
                AdminHuvudMeny meny = new AdminHuvudMeny();
                meny.setVisible(true);
                setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Login Failed.");
                anv_namn.setText("");
                los_ord.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_loggainbtnActionPerformed

