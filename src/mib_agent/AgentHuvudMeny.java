    private void bytlosenbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bytlosenbtnActionPerformed
        String nyttLosen = nyttlostxt.getText();
        if (Validering.textFaltHarVarde(nyttlostxt) && Validering.isHeltal(AgentIDlosbyt) && Validering.textFaltHarVarde(AgentIDlosbyt) && Validering.textFaltHarVarde(oldlosenagenttxt)) {
            if (Validering.giltigtLosenord(nyttLosen)) {
                try {
                    Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/mibdb", "mibdba", "mibkey");
                    PreparedStatement ps = connection.prepareStatement("UPDATE agent set Losenord=? where Agent_ID=? AND Losenord=?");
                    ps.setString(1, nyttlostxt.getText());
                    ps.setString(2, AgentIDlosbyt.getText());
                    ps.setString(3, oldlosenagenttxt.getText());
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Lösenord uppdaterad!");
                    nyttlostxt.setText("");
                    AgentIDlosbyt.setText("");
                    oldlosenagenttxt.setText("");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "AgentID eller det gamla lösenordet är fel.");
                    nyttlostxt.setText("");
                    AgentIDlosbyt.setText("");
                    oldlosenagenttxt.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nytt lösenord måste bestå av minst 1 karaktär och max 6.\nVänligen försök igen.");
            }
        }
    }//GEN-LAST:event_bytlosenbtnActionPerformed

